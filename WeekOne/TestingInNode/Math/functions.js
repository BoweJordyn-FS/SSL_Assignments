function add(a, b) {
	return a + b;
}

function subtract(a, b) {
	return a - b;
}

function multiply(a, b) {
	return a * b;
}

function divide(a, b) {
	return a / b;
}
function sqrt(number) {
	return Math.sqrt(number);
}

function max(a, b) {
	return Math.max(a, b);
}

module.exports = {
	add,
	subtract,
	sqrt,
	multiply,
	divide,
	max,
};
